    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:

        if (txtVaccineQty.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter Quantity.");
        } else {
            ClinicWorkRequest cwr = new ClinicWorkRequest();
            cwr.setVaccine((Vaccine) cmbVaccine.getSelectedItem());
            cwr.setRequestedQty(Integer.parseInt(txtVaccineQty.getText()));
            cwr.setStatus("Sent to Provider");
            cwr.setSender(account);

            clinicOrganization.getWorkQueue().getWorkRequestList().add(cwr);
            business.getWorkQueue().getWorkRequestList().add(cwr);
            populateTbl();
            txtVaccineQty.setText("");
            JOptionPane.showMessageDialog(null, "Request created successfully.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnRequestActionPerformed

