    public void populateBox() {
        cmbVaccine.removeAllItems();
        for (Vaccine vaccine : business.getVaccineCatalog().getVaccineList()) {
            cmbVaccine.addItem(vaccine);
        }
    }

    void populateTbl() {

        DefaultTableModel dtm_ = (DefaultTableModel) tblRequestfromClinic.getModel();
        dtm_.setRowCount(0);
        for (WorkRequest work : clinicOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[8];
            row[0] = work.getVaccine().getVaccineId();
            row[1] = work.getVaccine();
            row[2] = work.getVaccine().getPrice();
            row[3] = ((ClinicWorkRequest) work).getRequestedQty();
            row[4] = work;
            row[5] = work.getReceiver();
            row[6] = work.getRequestDate();
            row[7] = work.getResolveDate();
            dtm_.addRow(row);

        }
    }

